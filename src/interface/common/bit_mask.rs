// BIT MASK FOR RESETTING MULTIPLE BITS
const ONE_BIT: u32 = 0x00000001;
const TWO_BIT: u32 = 0x00000003;
const THREE_BIT: u32 = 0x00000007;
const FOUR_BIT: u32 = 0x0000000F;
const FIVE_BIT: u32 = 0x0000001F;
const SIX_BIT: u32 = 0x0000003F;
const SEVEN_BIT: u32 = 0x0000007F;
const EIGHT_BIT: u32 = 0x000000FF;
const NINE_BIT: u32 = 0x000001FF;
const TEN_BIT: u32 = 0x000003FF;
const ELEVEN_BIT: u32 = 0x000007FF;
const TWELVE_BIT: u32 = 0x00000FFF;
const THIRTEEN_BIT: u32 = 0x00001FFF;
const FOURTEEN_BIT: u32 = 0x00003FFF;
const FIFTEEN_BIT: u32 = 0x00007FFF;
const SIXTEEN_BIT: u32 = 0x0000FFFF;

pub fn get_one_bit_mask() -> u32 {
    return ONE_BIT;
}

pub fn get_two_bit_mask() -> u32 {
    return TWO_BIT;
}

pub fn get_three_bit_mask() -> u32 {
    return THREE_BIT;
}

pub fn get_four_bit_mask() -> u32 {
    return FOUR_BIT;
}

